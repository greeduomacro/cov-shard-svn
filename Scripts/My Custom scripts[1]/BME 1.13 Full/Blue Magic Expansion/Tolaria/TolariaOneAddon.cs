	public class BlueMageEntryAddonComponent : AddonComponent
	{
		private Point3D m_TargetLocation;
		private Map m_TargetMap;

		[CommandProperty( AccessLevel.GameMaster )]
		public Point3D TargetLocation
		{
			get { return m_TargetLocation; }
			set { m_TargetLocation = value; }
		}

		[CommandProperty( AccessLevel.GameMaster )]
		public Map TargetMap
		{
			get { return m_TargetMap; }
			set { m_TargetMap = value; }
		}

		public BlueMageEntryAddonComponent( int itemid, int hue ) : base( itemid )
		{
			Hue = hue;
		}

		public override void OnDoubleClick( Mobile from )
		{
			if ( from.InRange( this, 2 ) )
			{
				if ( !BlueMageControl.IsBlueMage( from ) )
					from.SendMessage( 1365, "Only Blue Mages are permitted entry." );
				else
					from.SendMessage( 1365, "Tolaria is not open to the public yet." );
			}
			else
				from.SendLocalizedMessage( 500295 ); // You are too far away to do that.
		}

		public BlueMageEntryAddonComponent( Serial serial ) : base( serial )
		{
		}

		public override void Serialize( GenericWriter writer )
		{
			base.Serialize( writer );
			writer.Write( (int) 0 ); // version

			writer.Write( m_TargetLocation );
			writer.Write( m_TargetMap );
		}

		public override void Deserialize( GenericReader reader )
		{
			base.Deserialize( reader );
			int version = reader.ReadInt();

			m_TargetLocation = reader.ReadPoint3D();
			m_TargetMap = reader.ReadMap();
		}
	}
